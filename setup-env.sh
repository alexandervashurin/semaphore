#!/bin/bash
# ============================================================================
# Semaphore UI - Настройка .env файла
# ============================================================================
# Этот скрипт автоматически создаёт или обновляет .env файл
# с необходимыми переменными окружения для запуска проекта
# ============================================================================

set -e

# Цвета для вывода
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
BLUE='\033[0;34m'
CYAN='\033[0;36m'
NC='\033[0m' # No Color

# Функции для вывода
info() {
    echo -e "${BLUE}ℹ️  $1${NC}"
}

success() {
    echo -e "${GREEN}✅ $1${NC}"
}

warning() {
    echo -e "${YELLOW}⚠️  $1${NC}"
}

error() {
    echo -e "${RED}❌ $1${NC}"
    exit 1
}

header() {
    echo -e "${CYAN}╔════════════════════════════════════════════════════════╗${NC}"
    echo -e "${CYAN}║${NC} $1"
    echo -e "${CYAN}╚════════════════════════════════════════════════════════╝${NC}"
    echo ""
}

# Директория проекта
SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
ENV_FILE="$SCRIPT_DIR/.env"
ENV_EXAMPLE="$SCRIPT_DIR/.env.example"

# ============================================================================
# Проверка наличия Docker и PostgreSQL
# ============================================================================

check_docker() {
    if command -v docker &> /dev/null; then
        return 0
    fi
    return 1
}

check_postgres() {
    if docker-compose ps 2>/dev/null | grep -q "semaphore-db.*Healthy"; then
        return 0
    fi
    return 1
}

# ============================================================================
# Генерация случайной строки
# ============================================================================

generate_secret() {
    openssl rand -base64 32 | tr -dc 'a-zA-Z0-9' | head -c 64
}

# ============================================================================
# Создание .env файла
# ============================================================================

create_env_file() {
    header "Настройка .env файла"
    
    # Проверка существующего файла
    if [ -f "$ENV_FILE" ]; then
        echo ""
        echo -e "${YELLOW}⚠️  Файл .env уже существует!${NC}"
        echo ""
        read -p "Хотите перезаписать? (y/N): " -n 1 -r
        echo ""
        if [[ ! $REPLY =~ ^[Yy]$ ]]; then
            info "Отменено пользователем"
            exit 0
        fi
        
        # Создаём резервную копию
        cp "$ENV_FILE" "$ENV_FILE.backup.$(date +%Y%m%d_%H%M%S)"
        success "Создана резервная копия: $ENV_FILE.backup.*"
        echo ""
    fi
    
    # Меню выбора типа БД
    echo ""
    echo "Выберите тип базы данных:"
    echo "  1) SQLite (по умолчанию, для тестирования)"
    echo "  2) PostgreSQL (рекомендуется для продакшена)"
    echo "  3) PostgreSQL с демо-данными (Docker)"
    echo ""
    read -p "Ваш выбор (1-3): " db_choice
    
    case $db_choice in
        1)
            # SQLite
            info "Настройка SQLite..."
            create_sqlite_env
            ;;
        2)
            # PostgreSQL
            info "Настройка PostgreSQL..."
            create_postgres_env
            ;;
        3)
            # PostgreSQL с демо-данными
            info "Настройка PostgreSQL с демо-данными..."
            create_demo_env
            ;;
        *)
            error "Неверный выбор"
            ;;
    esac
    
    # Дополнительные настройки
    echo ""
    echo "══════════════════════════════════════════════════════"
    echo "Дополнительные настройки"
    echo "══════════════════════════════════════════════════════"
    echo ""
    
    # Порт
    read -p "Порт для сервера (по умолчанию 3000): " port
    port=${port:-3000}
    
    # Логирование
    echo ""
    echo "Уровень логирования:"
    echo "  1) error - только ошибки"
    echo "  2) warn - предупреждения"
    echo "  3) info - информация (по умолчанию)"
    echo "  4) debug - отладка"
    echo ""
    read -p "Ваш выбор (1-4): " log_choice
    
    case $log_choice in
        1) log_level="error" ;;
        2) log_level="warn" ;;
        3) log_level="info" ;;
        4) log_level="debug" ;;
        *) log_level="info" ;;
    esac
    
    # Обновляем файл с дополнительными настройками
    cat >> "$ENV_FILE" <<EOF

# ============================================================================
# Дополнительные настройки (добавлено скриптом)
# ============================================================================

# Порт сервера
SEMAPHORE_TCP_ADDRESS=0.0.0.0:$port

# Уровень логирования
RUST_LOG=$log_level

# Путь к web файлам
SEMAPHORE_WEB_PATH=./web/public

# Временная директория
SEMAPHORE_TMP_PATH=/tmp/semaphore
EOF

    success ".env файл создан и настроен!"
    echo ""
    show_summary
}

# ============================================================================
# SQLite конфигурация
# ============================================================================

create_sqlite_env() {
    cat > "$ENV_FILE" <<EOF
# ============================================================================
# Semaphore UI - Конфигурация (SQLite)
# Сгенерировано скриптом: $(date)
# ============================================================================

# -----------------------------------------------------------------------------
# База данных - SQLite
# -----------------------------------------------------------------------------
SEMAPHORE_DB_DIALECT=sqlite
SEMAPHORE_DB_PATH=/tmp/semaphore.db
SEMAPHORE_DB_URL=sqlite:///tmp/semaphore.db

# -----------------------------------------------------------------------------
# JWT Secret (генерируется автоматически)
# -----------------------------------------------------------------------------
SEMAPHORE_JWT_SECRET=$(generate_secret)

EOF

    success "SQLite конфигурация записана"
}

# ============================================================================
# PostgreSQL конфигурация
# ============================================================================

create_postgres_env() {
    echo ""
    echo "Введите параметры PostgreSQL:"
    echo ""
    
    read -p "Хост (по умолчанию localhost): " db_host
    db_host=${db_host:-localhost}
    
    read -p "Порт (по умолчанию 5432): " db_port
    db_port=${db_port:-5432}
    
    read -p "Имя пользователя (по умолчанию semaphore): " db_user
    db_user=${db_user:-semaphore}
    
    read -p "Пароль (по умолчанию semaphore_pass): " db_pass
    db_pass=${db_pass:-semaphore_pass}
    
    read -p "Имя базы данных (по умолчанию semaphore): " db_name
    db_name=${db_name:-semaphore}
    
    # Формируем URL
    db_url="postgres://${db_user}:${db_pass}@${db_host}:${db_port}/${db_name}"
    
    cat > "$ENV_FILE" <<EOF
# ============================================================================
# Semaphore UI - Конфигурация (PostgreSQL)
# Сгенерировано скриптом: $(date)
# ============================================================================

# -----------------------------------------------------------------------------
# База данных - PostgreSQL
# -----------------------------------------------------------------------------
SEMAPHORE_DB_DIALECT=postgres
SEMAPHORE_DB_URL=${db_url}

# -----------------------------------------------------------------------------
# JWT Secret (генерируется автоматически)
# -----------------------------------------------------------------------------
SEMAPHORE_JWT_SECRET=$(generate_secret)

EOF

    success "PostgreSQL конфигурация записана"
    echo ""
    info "URL подключения: $db_url"
}

# ============================================================================
# Демо конфигурация (PostgreSQL с демо-данными)
# ============================================================================

create_demo_env() {
    # Проверяем Docker
    if ! check_docker; then
        warning "Docker не найден. Демо-данные не будут загружены."
        echo ""
        read -p "Продолжить с обычной PostgreSQL конфигурацией? (y/N): " -n 1 -r
        echo ""
        if [[ $REPLY =~ ^[Yy]$ ]]; then
            create_postgres_env
            return
        else
            exit 1
        fi
    fi
    
    # Проверяем запущен ли PostgreSQL с демо-данными
    if check_postgres; then
        success "PostgreSQL с демо-данными запущен"
        db_url="postgres://semaphore:semaphore_pass@localhost:5432/semaphore"
    else
        echo ""
        warning "PostgreSQL с демо-данными не запущен"
        echo ""
        echo "Для запуска выполните:"
        echo "  ./demo-start.sh"
        echo ""
        read -p "Продолжить с демо-конфигурацией? (y/N): " -n 1 -r
        echo ""
        if [[ ! $REPLY =~ ^[Yy]$ ]]; then
            exit 0
        fi
        
        db_url="postgres://semaphore:semaphore_pass@localhost:5432/semaphore"
    fi
    
    cat > "$ENV_FILE" <<EOF
# ============================================================================
# Semaphore UI - Конфигурация (PostgreSQL с демо-данными)
# Сгенерировано скриптом: $(date)
# ============================================================================
# Для запуска демо-окружения выполните:
#   ./demo-start.sh
# ============================================================================

# -----------------------------------------------------------------------------
# База данных - PostgreSQL (Демо-данные)
# -----------------------------------------------------------------------------
SEMAPHORE_DB_DIALECT=postgres
SEMAPHORE_DB_URL=${db_url}

# -----------------------------------------------------------------------------
# JWT Secret (генерируется автоматически)
# -----------------------------------------------------------------------------
SEMAPHORE_JWT_SECRET=$(generate_secret)

# -----------------------------------------------------------------------------
# Демо режим
# -----------------------------------------------------------------------------
SEMAPHORE_DEMO_MODE=true

EOF

    success "Демо конфигурация записана"
    echo ""
    info "URL подключения: $db_url"
    echo ""
    echo "Учетные данные для входа:"
    echo "  - admin / demo123"
    echo "  - john.doe / demo123"
    echo "  - jane.smith / demo123"
    echo "  - devops / demo123"
}

# ============================================================================
# Показывает итоговую сводку
# ============================================================================

show_summary() {
    echo ""
    echo "══════════════════════════════════════════════════════"
    echo "Сводка"
    echo "══════════════════════════════════════════════════════"
    echo ""
    
    echo "Файл: $ENV_FILE"
    echo ""
    echo "Содержимое .env:"
    echo "──────────────────────────────────────────────────────"
    cat "$ENV_FILE"
    echo "──────────────────────────────────────────────────────"
    echo ""
    
    echo "Следующие шаги:"
    echo ""
    echo "1. Запуск Docker (PostgreSQL + Frontend):"
    echo "   ./demo-start.sh"
    echo ""
    echo "2. Запуск Backend (в новом терминале):"
    echo "   ./demo-start.sh --backend"
    echo ""
    echo "3. Откройте браузер:"
    echo "   http://localhost/demo-crud.html"
    echo ""
    echo "4. Документация:"
    echo "   - CRUD_DEMO.md"
    echo "   - ЗАПУСК_ДЕМО.md"
    echo ""
}

# ============================================================================
# Основная функция
# ============================================================================

main() {
    header "Semaphore UI - Настройка окружения"
    
    # Проверка, что мы в директории проекта
    if [ ! -f "$ENV_EXAMPLE" ]; then
        error "Файл .env.example не найден. Убедитесь, что скрипт запущен из корня проекта."
    fi
    
    # Переход в директорию проекта
    cd "$SCRIPT_DIR"
    
    # Создание .env файла
    create_env_file
    
    success "Настройка завершена!"
    echo ""
}

# Запуск
main "$@"
